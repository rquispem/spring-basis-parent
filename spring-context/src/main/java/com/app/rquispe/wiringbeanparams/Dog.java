package com.app.rquispe.wiringbeanparams;

public class Dog {

    public Dog() {
        System.out.println("dog created");
    }

    private String name;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    @Override
    public String toString() {
        return "Dog{" +
                "name='" + name + '\'' +
                '}';
    }
}
